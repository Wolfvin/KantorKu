"""
kantorku CLI — Command-line interface.

Commands:
    kantorku serve                Start the server (default)
    kantorku tui                  Launch the TUI for coders
    kantorku init                 Scaffold a new kantorku project
    kantorku worker create        Create a new plug-and-play worker
    kantorku worker add           Add a worker from an existing directory
    kantorku worker validate      Validate a worker directory
    kantorku worker list          List available workers
    kantorku config validate      Validate kantorku.toml
    kantorku run                  Run a one-shot task
    kantorku version              Show version
"""

from __future__ import annotations

import argparse
import asyncio
import json
import sys
from pathlib import Path


def cmd_serve(args: argparse.Namespace) -> None:
    """Start the kantorku server."""
    import uvicorn
    from kantorku.interface.server import create_office

    config_path = getattr(args, "config", None)
    office = create_office(config_path)

    uvicorn.run(
        "kantorku.interface.server:app",
        host=getattr(args, "host", "0.0.0.0"),
        port=getattr(args, "port", 8000),
        reload=getattr(args, "reload", False),
        lifespan="on",
    )


def cmd_init(args: argparse.Namespace) -> None:
    """Scaffold a new kantorku project."""
    project_dir = Path(getattr(args, "name", "my-office"))
    project_dir.mkdir(parents=True, exist_ok=True)

    # Create kantorku.toml
    toml_content = '''# kantorku configuration
# Generated by `kantorku init`

[conductor]
model = "anthropic/claude-opus-4-6"

[providers.anthropic]
api_key = "${ANTHROPIC_API_KEY}"

[providers.ollama]
base_url = "http://localhost:11434"

[workers.coder_frontend]
model = "anthropic/claude-sonnet-4-6"
squad = "coding"
role = "React/CSS/UI specialist"

[workers.coder_backend]
model = "ollama/llama3"
squad = "coding"
role = "Python/Rust/Systems specialist"

[workers.intake]
model = "ollama/llama3"
squad = "translation"
role = "Message parser"

[workers.narrator]
model = "ollama/llama3"
squad = "translation"
role = "Output formatter"

[pool]
model = "ollama/llama3"
instances = 1

[memory]
ring1_path = "data/ring1.duckdb"
ring2_path = "data/ring2.db"
ring3_enabled = false

[server]
host = "0.0.0.0"
port = 8000
'''

    (project_dir / "kantorku.toml").write_text(toml_content)

    # Create data directory
    (project_dir / "data").mkdir(exist_ok=True)

    # Create custom workers directory with README
    workers_dir = project_dir / "workers"
    workers_dir.mkdir(exist_ok=True)
    (workers_dir / ".gitkeep").touch()

    # Create a simple example
    example_content = '''"""Example: Using kantorku programmatically."""
import asyncio
from kantorku import Office, Hooks, HookType

async def main():
    # Create office with hooks
    hooks = Hooks()

    @hooks.on(HookType.ON_TASK_COMPLETED)
    async def on_done(event):
        print(f"Task completed: {event.get('task_id', 'unknown')}")

    office = Office(hooks=hooks)

    # Configure providers
    office.configure_provider("ollama", base_url="http://localhost:11434")

    # Hire workers
    office.hire_worker("coder_backend", model="ollama/llama3", role="Backend developer")

    # Initialize
    await office.initialize()

    # Run a task
    result = await office.run("Write a Python function to calculate fibonacci", auto_accept=True)
    print(result)

    await office.shutdown()

if __name__ == "__main__":
    asyncio.run(main())
'''

    (project_dir / "example.py").write_text(example_content)

    print(f"Created kantorku project in ./{project_dir}/")
    print(f"  - {project_dir}/kantorku.toml  (configuration)")
    print(f"  - {project_dir}/data/          (memory storage)")
    print(f"  - {project_dir}/workers/       (custom workers — drop worker folders here!)")
    print(f"  - {project_dir}/example.py     (example script)")
    print()
    print("Next steps:")
    print(f"  1. cd {project_dir}")
    print("  2. Edit kantorku.toml with your API keys")
    print("  3. Create a custom worker: kantorku worker create my_worker")
    print("  4. Run: kantorku serve --config kantorku.toml")


