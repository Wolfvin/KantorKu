def cmd_config_validate(args: argparse.Namespace) -> None:
    """Validate a kantorku.toml configuration file."""
    config_path = getattr(args, "config", None)
    if not config_path:
        print("Error: --config path required for validation")
        sys.exit(1)

    path = Path(config_path)
    if not path.exists():
        print(f"Error: Config file not found: {config_path}")
        sys.exit(1)

    try:
        from kantorku.config.settings import KantorkuConfig
        config = KantorkuConfig.from_toml(config_path)
        config.resolve_env_vars()

        errors = []
        warnings = []

        # Check conductor model
        if not config.conductor_model:
            errors.append("conductor.model is required")
        elif "/" not in config.conductor_model:
            errors.append(f"conductor.model must be 'provider/model' format, got: {config.conductor_model}")

        # Check workers
        if not config.workers:
            warnings.append("No workers configured")

        for wid, wcfg in config.workers.items():
            if wcfg.model and "/" not in wcfg.model:
                errors.append(f"worker '{wid}' model must be 'provider/model' format, got: {wcfg.model}")

        # Check providers
        for pid, pcfg in config.providers.items():
            api_key = pcfg.get("api_key", "")
            if api_key.startswith("${") and api_key.endswith("}"):
                env_var = api_key[2:-1]
                import os
                if not os.environ.get(env_var):
                    warnings.append(f"provider '{pid}' api_key env var ${env_var} is not set")

        # Print results
        if errors:
            print("Validation FAILED:")
            for e in errors:
                print(f"  ERROR: {e}")
            sys.exit(1)

        if warnings:
            print("Validation passed with warnings:")
            for w in warnings:
                print(f"  WARNING: {w}")
        else:
            print("Configuration is valid!")

        print(f"  Conductor: {config.conductor_model}")
        print(f"  Workers:   {len(config.workers)}")
        print(f"  Providers: {len(config.providers)}")
        print(f"  Pool:      {config.pool.instances} instances of {config.pool.model}")

    except Exception as e:
        print(f"Validation FAILED: {e}")
        sys.exit(1)


def cmd_run(args: argparse.Namespace) -> None:
    """Run a one-shot task."""
    message = getattr(args, "message", None)
    if not message:
        print("Error: Message required. Usage: kantorku run 'your task description'")
        sys.exit(1)

    async def _run() -> None:
        from kantorku.office import Office

        config_path = getattr(args, "config", None)
        if config_path:
            office = Office.from_config(config_path)
        else:
            office = Office()

        await office.initialize()

        auto_accept = getattr(args, "auto_accept", False)
        result = await office.run(message, auto_accept=auto_accept)
        print(json.dumps(result, indent=2, default=str))

        await office.shutdown()

    asyncio.run(_run())


def cmd_tui(args: argparse.Namespace) -> None:
    """Launch the KantorKu TUI for coders."""
    try:
        from kantorku.tui.app import KantorKuTUI, EmbeddedKantorKuTUI
    except ImportError as e:
        print("Error: TUI dependencies not installed.")
        print("  Install with: pip install kantorku[tui]")
        print(f"  Details: {e}")
        sys.exit(1)

    server_url = getattr(args, "url", None) or "http://localhost:8000"
    config_path = getattr(args, "config", None)
    embedded = getattr(args, "embedded", False)

    if embedded:
        # Embedded mode — run Office directly in-process
        app = EmbeddedKantorKuTUI(config_path=config_path)
    else:
        # Remote mode — connect to a running server
        app = KantorKuTUI(server_url=server_url, config_path=config_path)

    app.run()


def cmd_version(args: argparse.Namespace) -> None:
    """Show kantorku version."""
    from kantorku import __version__
    print(f"kantorku v{__version__}")


def main() -> None:
    """CLI entry point."""
    parser = argparse.ArgumentParser(
        prog="kantorku",
        description="kantorku — Kantor digital yang sesungguhnya",
    )
    parser.add_argument("--config", "-c", help="Path to kantorku.toml")
    subparsers = parser.add_subparsers(dest="command", help="Available commands")

    # serve
    serve_parser = subparsers.add_parser("serve", help="Start the kantorku server")
    serve_parser.add_argument("--host", default="0.0.0.0", help="Host to bind")
    serve_parser.add_argument("--port", "-p", type=int, default=8000, help="Port to bind")
    serve_parser.add_argument("--reload", action="store_true", help="Enable auto-reload")

    # init
    init_parser = subparsers.add_parser("init", help="Scaffold a new kantorku project")
    init_parser.add_argument("name", nargs="?", default="my-office", help="Project directory name")

    # worker subcommand group
    worker_parser = subparsers.add_parser("worker", help="Worker management commands")
    worker_subparsers = worker_parser.add_subparsers(dest="worker_command", help="Worker commands")

    # worker create
    create_parser = worker_subparsers.add_parser(
        "create", help="Create a new plug-and-play worker",
        aliases=["new"],
    )
    create_parser.add_argument("name", help="Worker ID (alphanumeric + underscores)")
    create_parser.add_argument("--model", "-m", default="ollama/llama3", help="LLM model (provider/model)")
    create_parser.add_argument("--squad", "-s", default="support",
                               choices=["coding", "verification", "support", "translation"],
                               help="Squad assignment")
    create_parser.add_argument("--role", "-r", default="", help="Role description")
    create_parser.add_argument("--capabilities", "-cap", default="", help="Comma-separated capabilities")
    create_parser.add_argument("--category", "-cat", default="", help="Top-level category (coding, verification, support, translation)")
    create_parser.add_argument("--subcategory", "-sub", default="", help="Sub-category (e.g. frontend, backend, debugging)")
    create_parser.add_argument("--display-name", "-dn", default="", help="Human-friendly display name")
    create_parser.add_argument("--tags", "-t", default="", help="Comma-separated tags for AI agent organization")
    create_parser.add_argument("--description", "-d", default="", help="Short description")
    create_parser.add_argument("--base-dir", default="workers", help="Parent directory for workers")
    create_parser.add_argument("--overwrite", "-f", action="store_true", help="Overwrite existing files")

    # worker add
    add_parser = worker_subparsers.add_parser("add", help="Add a worker from an existing directory")
    add_parser.add_argument("path", help="Path to worker directory")
    add_parser.add_argument("--model", "-m", default="", help="Override model")
    add_parser.add_argument("--squad", "-s", default="", help="Override squad")
    add_parser.add_argument("--role", "-r", default="", help="Override role")

    # worker validate
    validate_parser = worker_subparsers.add_parser("validate", help="Validate a worker directory")
    validate_parser.add_argument("path", help="Path to worker directory")

    # worker list
    worker_subparsers.add_parser("list", help="List available workers")

    # config validate
    subparsers.add_parser("config-validate", help="Validate kantorku.toml")

    # run
    run_parser = subparsers.add_parser("run", help="Run a one-shot task")
    run_parser.add_argument("message", help="Task description")
    run_parser.add_argument("--auto-accept", "-y", action="store_true", help="Auto-accept contract")

    # tui
    tui_parser = subparsers.add_parser("tui", help="Launch the TUI for coders")
    tui_parser.add_argument("--url", "-u", default="http://localhost:8000",
                             help="Server URL (default: http://localhost:8000)")
    tui_parser.add_argument("--embedded", "-e", action="store_true",
                             help="Run Office in-process (no server needed)")

    # version
    subparsers.add_parser("version", help="Show version")

    args = parser.parse_args()

    if args.command == "serve":
        cmd_serve(args)
    elif args.command == "init":
        cmd_init(args)
    elif args.command == "worker":
        if args.worker_command in ("create", "new"):
            cmd_worker_create(args)
        elif args.worker_command == "add":
            cmd_worker_add(args)
        elif args.worker_command == "validate":
            cmd_worker_validate(args)
        elif args.worker_command == "list":
            cmd_worker_list(args)
        else:
            worker_parser.print_help()
    elif args.command == "worker-list":
        # Backwards compat
        cmd_worker_list(args)
    elif args.command == "config-validate":
        cmd_config_validate(args)
    elif args.command == "run":
        cmd_run(args)
    elif args.command == "tui":
        cmd_tui(args)
    elif args.command == "version":
        cmd_version(args)
    else:
        # Default: start server
        cmd_serve(args)


if __name__ == "__main__":
    main()
