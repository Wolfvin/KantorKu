"""
LibrarianRecipe — Fine-tuning recipe for the Librarian model.

Defines the hyperparameters, model architecture, and training configuration
for fine-tuning the Losion Librarian model. The recipe supports serialization
to/from YAML for configuration management and reproducibility.

The default configuration targets the ``Wolfvin/losion-1b-base`` model with
partial layer freezing (layers 8-11 + lm_head), bf16 mixed precision, and
a target JSON parse rate of 98%.

Example::

    recipe = LibrarianRecipe()
    print(recipe.to_yaml())

    # Customize
    recipe = LibrarianRecipe(
        learning_rate=1e-5,
        max_steps=10000,
        batch_size=16,
    )

    # Save and load
    yaml_str = recipe.to_yaml()
    restored = LibrarianRecipe.from_yaml(yaml_str)
    assert restored.learning_rate == 1e-5
"""

from __future__ import annotations

import logging
from dataclasses import dataclass, field, fields
from typing import Any

logger = logging.getLogger(__name__)


@dataclass
class LibrarianRecipe:
    """Fine-tuning recipe for the Librarian model.

    Contains all hyperparameters, model configuration, and training
    settings needed to reproduce a Librarian fine-tuning run. The recipe
    can be serialized to YAML for storage and loaded back via
    :meth:`from_yaml`.

    Attributes:
        base_model: HuggingFace model identifier for the base model.
        d_model: Hidden dimension of the transformer model.
        n_layers: Total number of transformer layers.
        batch_size: Training batch size (per GPU).
        learning_rate: Peak learning rate for the optimizer.
        warmup_steps: Number of linear warmup steps.
        max_steps: Total number of training steps.
        grad_clip: Maximum gradient norm for clipping.
        precision: Training precision ("bf16", "fp16", or "fp32").
        freeze_layers: Whether to freeze early layers during fine-tuning.
        trainable_layers: Layer indices or names that remain trainable
            when ``freeze_layers`` is True. Can include integer indices
            or string names like "lm_head".
        max_seq_len: Maximum sequence length for tokenization.
        eval_steps: Run evaluation every N steps.
        save_steps: Save a checkpoint every N steps.
        target_json_parse_rate: Target JSON parse success rate for the
            Librarian's structured output. Training is considered
            successful when this rate is achieved.
        hub_repo: HuggingFace Hub repository to push the fine-tuned
            model to.
    """

    # ── Model configuration ───────────────────────────────────────────────
    base_model: str = "Wolfvin/losion-1b-base"
    d_model: int = 768
    n_layers: int = 12

    # ── Training hyperparameters ──────────────────────────────────────────
    batch_size: int = 32
    learning_rate: float = 2e-5
    warmup_steps: int = 200
    max_steps: int = 5000
    grad_clip: float = 1.0
    precision: str = "bf16"

    # ── Layer freezing ────────────────────────────────────────────────────
    freeze_layers: bool = True
    trainable_layers: list[int | str] = field(
        default_factory=lambda: [8, 9, 10, 11, "lm_head"]
    )

    # ── Sequence and checkpointing ────────────────────────────────────────
    max_seq_len: int = 4096
    eval_steps: int = 250
    save_steps: int = 500

    # ── Evaluation target ─────────────────────────────────────────────────
    target_json_parse_rate: float = 0.98

    # ── Output ────────────────────────────────────────────────────────────
    hub_repo: str = "Wolfvin/losion-librarian-1b"

    def to_yaml(self) -> str:
        """Generate a YAML configuration string from this recipe.

        Produces a structured YAML document with sections for model,
        training, data, output, and evaluation configuration. The output
        is suitable for passing to a training script or storing as a
        configuration file.

        Returns:
            A YAML-formatted string representing the recipe.
        """
        # Format trainable_layers as YAML list
        trainable_yaml = "\n".join(
            f"  - {item!r}" if isinstance(item, str) else f"  - {item}"
            for item in self.trainable_layers
        )

        yaml_str = f"""\
# Librarian Fine-tuning Recipe
# Generated by LibrarianRecipe.to_yaml()

model:
  base_model: {self.base_model}
  d_model: {self.d_model}
  n_layers: {self.n_layers}
  max_seq_len: {self.max_seq_len}
  precision: {self.precision}
  freeze_layers: {str(self.freeze_layers).lower()}
  trainable_layers:
{trainable_yaml}

training:
  batch_size: {self.batch_size}
  learning_rate: {self.learning_rate}
  warmup_steps: {self.warmup_steps}
  max_steps: {self.max_steps}
  grad_clip: {self.grad_clip}
  eval_steps: {self.eval_steps}
  save_steps: {self.save_steps}

data:
  train_file: train.jsonl
  val_file: val.jsonl
  max_seq_len: {self.max_seq_len}

output:
  hub_repo: {self.hub_repo}
  save_total_limit: 3
  push_to_hub: true

evaluation:
  target_json_parse_rate: {self.target_json_parse_rate}
  eval_on_start: false
  metric_for_best_model: json_parse_rate
"""
        return yaml_str

    def to_dict(self) -> dict[str, Any]:
        """Convert the recipe to a plain dictionary.

        Returns:
            A dict mapping field names to their values. List values
            (like ``trainable_layers``) are copied to prevent mutation.
        """
        result: dict[str, Any] = {}
        for f in fields(self):
            value = getattr(self, f.name)
            # Copy lists to prevent external mutation
            if isinstance(value, list):
                value = list(value)
            result[f.name] = value
        return result

    @classmethod
    def from_yaml(cls, yaml_str: str) -> LibrarianRecipe:
        """Parse a YAML configuration string into a LibrarianRecipe.

        Supports both the structured format produced by :meth:`to_yaml`
        and a flat key-value format. Unknown keys are ignored.

        Args:
            yaml_str: A YAML-formatted string containing recipe
                configuration.

        Returns:
            A LibrarianRecipe instance with values populated from the
            YAML string.

        Raises:
            ValueError: If the YAML cannot be parsed.
        """
        try:
            import yaml
        except ImportError:
            # Fallback: manual YAML-like parsing for simple cases
            return cls._parse_yaml_fallback(yaml_str)

        try:
            data = yaml.safe_load(yaml_str)
        except yaml.YAMLError as exc:
            raise ValueError(f"Invalid YAML: {exc}") from exc

        if not isinstance(data, dict):
            raise ValueError(f"Expected YAML dict, got {type(data).__name__}")

        # Flatten nested structure
        flat: dict[str, Any] = {}
        for section_key, section_val in data.items():
            if isinstance(section_val, dict):
                for k, v in section_val.items():
                    flat[k] = v
            else:
                flat[section_key] = section_val

        # Build kwargs for the dataclass
        kwargs: dict[str, Any] = {}

        # Map flat keys to dataclass fields with type coercion
        field_types = {f.name: f.type for f in fields(cls)}

        type_defaults: dict[str, Any] = {
            "base_model": "",
            "d_model": 0,
            "n_layers": 0,
            "batch_size": 0,
            "learning_rate": 0.0,
            "warmup_steps": 0,
            "max_steps": 0,
            "grad_clip": 0.0,
            "precision": "",
            "freeze_layers": False,
            "max_seq_len": 0,
            "eval_steps": 0,
            "save_steps": 0,
            "target_json_parse_rate": 0.0,
            "hub_repo": "",
        }

        for key, value in flat.items():
            if key not in type_defaults and key != "trainable_layers":
                # Skip unknown keys
                continue

            if key == "trainable_layers":
                # Handle trainable_layers specially: must be list of int|str
                if isinstance(value, list):
                    coerced: list[int | str] = []
                    for item in value:
                        if isinstance(item, (int, str)):
                            coerced.append(item)
                        elif isinstance(item, float) and item == int(item):
                            coerced.append(int(item))
                        else:
                            coerced.append(str(item))
                    kwargs["trainable_layers"] = coerced
                continue

            # Get expected type from defaults
            default_val = type_defaults.get(key)
            if default_val is None:
                continue

            # Coerce value type
            try:
                if isinstance(default_val, bool):
                    # Boolean coercion (handle string "true"/"false")
                    if isinstance(value, str):
                        kwargs[key] = value.lower() in ("true", "1", "yes")
                    else:
                        kwargs[key] = bool(value)
                elif isinstance(default_val, int):
                    kwargs[key] = int(value)
                elif isinstance(default_val, float):
                    kwargs[key] = float(value)
                elif isinstance(default_val, str):
                    kwargs[key] = str(value)
                else:
                    kwargs[key] = value
            except (TypeError, ValueError) as exc:
                logger.warning(
                    "Failed to coerce %s=%r: %s — using default", key, value, exc
                )

        return cls(**kwargs)

    @classmethod
    def _parse_yaml_fallback(cls, yaml_str: str) -> LibrarianRecipe:
        """Fallback YAML parser when PyYAML is not installed.

        Handles the simple structured format produced by :meth:`to_yaml`.
        This is not a general-purpose YAML parser — it only supports the
        specific format used by this class.

        Args:
            yaml_str: A YAML-formatted string.

        Returns:
            A LibrarianRecipe instance.
        """
        kwargs: dict[str, Any] = {}
        current_section: str | None = None
        trainable_layers: list[int | str] = []

        int_fields = {
            "d_model", "n_layers", "batch_size", "warmup_steps",
            "max_steps", "max_seq_len", "eval_steps", "save_steps",
        }
        float_fields = {
            "learning_rate", "grad_clip", "target_json_parse_rate",
        }
        str_fields = {"base_model", "precision", "hub_repo"}
        bool_fields = {"freeze_layers"}

        for line in yaml_str.splitlines():
            stripped = line.strip()

            # Skip comments and empty lines
            if not stripped or stripped.startswith("#"):
                continue

            # Detect section headers (e.g., "model:", "training:")
            if stripped.endswith(":") and not stripped.startswith("-"):
                current_section = stripped[:-1].strip()
                continue

            # Parse trainable_layers items
            if stripped.startswith("- ") and current_section == "model":
                item_str = stripped[2:].strip().strip("'\"")
                try:
                    trainable_layers.append(int(item_str))
                except ValueError:
                    trainable_layers.append(item_str)
                continue

            # Parse key-value pairs
            if ":" in stripped:
                key, _, value_str = stripped.partition(":")
                key = key.strip()
                value_str = value_str.strip()

                if key in int_fields:
                    try:
                        kwargs[key] = int(value_str)
                    except ValueError:
                        pass
                elif key in float_fields:
                    try:
                        kwargs[key] = float(value_str)
                    except ValueError:
                        pass
                elif key in str_fields:
                    kwargs[key] = value_str
                elif key in bool_fields:
                    kwargs[key] = value_str.lower() in ("true", "1", "yes")

        if trainable_layers:
            kwargs["trainable_layers"] = trainable_layers

        return cls(**kwargs)
